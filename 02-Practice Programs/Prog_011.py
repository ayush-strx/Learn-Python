# Convert an integer into a float.
a = 10
print(a, type(a))

b = float(a)
print(b, type(b))

#------------------------------------------------------------------------

# Convert a float  into an integer.
a = 10.2
print(a, type(a))

b = int(a)
print(b, type(b))

#------------------------------------------------------------------------

# Convert a string "100" into an integer.
a = "100"
print(a, type(a))

b = int(a)
print(b, type(b))

#------------------------------------------------------------------------


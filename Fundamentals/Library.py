#A library in Python is a collection of modules, functions, and classes that provide ready‑made code to perform specific tasks.
#There are two types of libraries in Python: Standard Library and Third‑Party Libraries.

#1.Standard Library:
#The Python Standard Library is a collection of built‑in modules, classes, functions, and methods that come bundled with Python, ready to use without extra installation

#Examples:
"""Built-in Data Types: int, float, str, list, dict, etc.
   Built-in Classes: class int, class str, class float, class bool, class list, class tuple, class set, class dict etc.
   Built-in Functions: print(), len(), type() , range(), input(), etc.
   Built in methods: append(), pop(), sort(), upper(), lower(), etc.
   Built-in Modules: math, random, datetime, os, sys, etc. (Import required to use these modules)"""

#2.Third‑Party Libraries:
#These are libraries created by the Python community that can be installed and used in your projects. Examples include NumPy (for numerical computing), Pandas (for data manipulation), Matplotlib (for data visualization) etc. (Installation required to use these libraries)




# Escape sequnces are used to represent special characters in strings. They are denoted by a backslash (\) followed by a specific character. Here are some common escape sequences:
# \' - Single quote
# \" - Double quote
# \n - New line
# \t - Tab
# \\ - Backslash

# Example usage of escape sequences
# Single quote
print('It\'s a beautiful day!')

# Double quote
print("He said, \"Hello, how are you?\"")

# New line
print("Hello\nWorld")

# Tab
print("Hello\tWorld")

# Backslash
print("This is a backslash: \\")


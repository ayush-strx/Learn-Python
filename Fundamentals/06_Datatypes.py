#Data types define the kind of data a variable can store (like numbers, characters, text, etc.).
# There are two types of data types in python:

# A. Primitive data types: These are the basic data types that store simple values.
# Examples include integers, floats, strings, and booleans.

#1. Integer (int): Used to store whole numbers.
age = 25    
print(age)

#2. Float (float): Used to store decimal numbers.
pi = 3.14
print(pi)

#3. String (str): Used to store text.
name = "Ayush"
print(name)

#4. Boolean (bool): Used to store True or False values. Used to handle logic and decisions making in programs.
is_student = True
print(is_student)

# B. Non-primitive data types: These are more complex data types that can store multiple values or collections of data.  
# Examples include lists, tuples, sets, and dictionaries.
           
#1. List (list): Used to store multiple items in a single variable. Ordered and changeable.
fruits = ["apple", "banana", "cherry"]
print(fruits)

#2. Tuple (tuple): Similar to lists but cannot be changed after creation. Ordered and unchangeable.
coordinates = (10, 20)  
print(coordinates)

#3. Set (set): Used to store unique items. Unordered and unindexed.
unique_numbers = {1, 2, 3, 4, 5}
print(unique_numbers)

#4. Dictionary (dict): Used to store data in key-value pairs.
person = {"name": "Ayush", "age": 19}
print(person)                               
